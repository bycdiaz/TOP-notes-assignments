# In irb, use these methods to find out if certain numbers are odd or even. 
# Numbers like 0, 1, 2, 99, -502 etc.

# You can use a method by appending a dot . and then the method name to the 
# object. E.g. -99.abs uses (we also say: “calls”) the method abs on the number -99.

# Try for yourself what it does, and google for “ruby abs” to find the 
# documentation for this method.

puts 0.even?

puts 1.even?

puts 2.even?

puts 99.even?

puts -502.even?
