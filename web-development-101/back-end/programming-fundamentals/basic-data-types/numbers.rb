# Addition
1 + 1   #=> 2

# Subtraction
2 - 1   #=> 1

# Multiplication
2 * 2   #=> 4

# Division
10 / 5  #=> 2

# Exponent
2 ** 2  #=> 4
3 ** 4  #=> 81

# Modulus (find the remainder of division)
8 % 2   #=> 0  (8 / 2 = 4; no remainder)
10 % 4  #=> 2  (10 / 4 = 2 with a remainder of 2)

# integers and floats

# integers are whole numbers. Floats contain decimals.

# It's important to keep in mind that when doing arithmetic with two 
# integers in Ruby, the result will always be an integer.

17 / 5    #=> 3, not 3.4

# To obtain an accurate answer, just replace one of the integers in the 
# expression with a float.

17 / 5.0  #=> 3.4