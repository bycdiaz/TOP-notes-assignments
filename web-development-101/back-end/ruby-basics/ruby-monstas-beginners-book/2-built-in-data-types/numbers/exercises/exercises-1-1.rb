# How many hours are in a year.

hours_in_year = 24 * 365

puts "There are #{hours_in_year} hours in a year."

# How many minutes are in a decade?

minutes = 60

hours = 24

year = 365

mins_decade = minutes * hours * year * 10

puts "There are #{mins_decade} minutes in a decade."

# How many seconds old are you?

seconds = 60

minutes = 60

hours = 24

year = 365

age_in_seconds = seconds * minutes * hours * year * 32

puts "I am #{age_in_seconds} seconds old."